random.seed(20260413)


def make_cover(idx: int, chapter: str, sub: str) -> str:
    c1, c2 = random.choice(PALETTES)
    svg = f"""<svg xmlns='http://www.w3.org/2000/svg' width='1200' height='720' viewBox='0 0 1200 720'>
  <defs><linearGradient id='bg' x1='0' y1='0' x2='1' y2='1'>
    <stop offset='0%' stop-color='{c1}'/><stop offset='100%' stop-color='{c2}'/>
  </linearGradient></defs>
  <rect width='1200' height='720' fill='url(#bg)' rx='34'/>
  <circle cx='920' cy='140' r='200' fill='rgba(255,255,255,0.08)'/>
  <text x='86' y='122' fill='#FFE4C0' font-size='30' font-family='Microsoft YaHei'>{chapter}</text>
  <text x='86' y='258' fill='#FFF8F0' font-size='64' font-weight='700' font-family='Microsoft YaHei'>{idx+1:02d}</text>
  <text x='86' y='336' fill='rgba(255,241,230,0.9)' font-size='28' font-family='Microsoft YaHei'>{sub}</text>
</svg>"""
    cover_dir = BASE_DIR / "storage" / "covers"
    cover_dir.mkdir(parents=True, exist_ok=True)
    path = cover_dir / f"content_{idx+1:03d}.svg"
    path.write_text(svg, encoding="utf-8")
    return f"/static/covers/content_{idx+1:03d}.svg"


def main():
    db = SessionLocal()
    try:
        # Clear all existing contents
        db.query(Content).delete()
        db.commit()
        print("Cleared all contents")

        now = datetime.now()
        for idx, (title, chapter, sub, tag) in enumerate(CONTENT_PLAN):
            cover = make_cover(idx, chapter, sub)
            body = random.choice(BODY_TEMPLATES).format(title=title)
            summary = f"{title}，国家级非物质文化遗产代表性项目，涵盖历史脉络、技艺特征与当代传承价值。"

            c = Content(
                title=title,
                cover_url=cover,
                content_type="article",
                source_site="中国非物质文化遗产网",
                summary=summary,
                body=body,
                chapter=chapter,
                sub_chapter=sub,
                content_hash=f"h_{idx+1:03d}",
                quality_score=round(random.uniform(0.88, 0.98), 2),
                review_status="approved",
                is_featured=(idx < 8),
                status="published",
                published_at=now - timedelta(hours=idx * 3),
                created_by=-99,
            )
            db.add(c)

        db.commit()
        print(f"Created {len(CONTENT_PLAN)} contents")

        # Report
        from sqlalchemy import func
        dist = db.query(Content.chapter, func.count(Content.id)).group_by(Content.chapter).order_by(
            func.count(Content.id).desc()
        ).all()
        print("\nCategory distribution:")
        for ch, cnt in dist:
            bar = "█" * cnt
            print(f"  {ch:<20}: {cnt:>2} {bar}")
        print(f"\nTotal: {sum(c for _, c in dist)}")

    finally:
        db.close()


if __name__ == "__main__":
    main()
