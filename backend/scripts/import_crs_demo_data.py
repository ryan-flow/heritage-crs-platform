def make_cover(filename, chapter, title, subtitle, line1, line2):
    c1, c2 = random.choice(PALETTES)
    svg = SVG_TEMPLATE.format(
        c1=c1, c2=c2,
        chapter=chapter[:8], title=title[:10], subtitle=subtitle[:12],
        line1=line1[:28], line2=line2[:28],
    )
    path = COVERS_DIR / filename
    path.write_text(svg, encoding="utf-8")
    return f"/static/covers/{filename}"


