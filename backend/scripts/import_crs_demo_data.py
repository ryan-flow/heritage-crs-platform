# ─── 主函数 ────────────────────────────────
def main():
    db = SessionLocal()
    try:
        now = datetime.now()

        # 1. 写入 contents（15条）
        all_articles = (
            [(a, "cold_start") for a in COLD_START_ARTICLES]
            + [(a, "interest") for a in INTEREST_ARTICLES]
            + [(a, "precision") for a in PRECISION_ARTICLES]
        )
        for i, (article, layer) in enumerate(all_articles):
            cover = make_cover(
                f"crs_demo_content_{i+1}.svg",
                article["chapter"], article["title"],
                article["sub_chapter"], "CRS演示专题内容", "非遗文化平台",
            )
            db.add(Content(
                title=article["title"], cover_url=cover, content_type="article",
                source_site="非遗文化平台 CRS演示数据",
                summary=article["summary"], body=article["body"],
                chapter=article["chapter"], sub_chapter=article["sub_chapter"],
                content_hash=f"crs_demo_{layer}_{i+1}",
                quality_score=0.90 if layer != "cold_start" else 0.85,
                review_status="approved", import_batch="crs_demo_v1",
                is_featured=1 if i < 5 else 0,
                status="published", published_at=now, created_by=-88, created_at=now,
            ))
        db.commit()
        print(f"[1] contents +{len(all_articles)}")

        # 2. 写入 discussion_topics（12条）
        topic_id_map = {}
        for i, topic in enumerate(DISCUSSION_TOPICS):
            dt = DiscussionTopic(
                user_id=topic["user_id"], nickname=topic["nickname"],
                title=topic["title"], content=topic["content"],
                cover_url=None, image_urls="[]",
                is_featured=bool(topic["is_featured"]),
                like_count=topic["like_count"],
                favorite_count=topic["favorite_count"],
                comment_count=topic["comment_count"],
                created_at=now + timedelta(hours=i),
            )
            db.add(dt)
            db.flush()
            topic_id_map[i] = dt.id
        db.commit()
        print(f"[2] discussion_topics +{len(DISCUSSION_TOPICS)}")

        # 3. 写入 discussion_topic_tags
        tag_count = 0
        for i, topic in enumerate(DISCUSSION_TOPICS):
            for tag in topic["tags"]:
                db.add(DiscussionTopicTag(
                    topic_id=topic_id_map[i], tag=tag,
                    created_at=now + timedelta(hours=i),
                ))
                tag_count += 1
        db.commit()
        print(f"[3] discussion_topic_tags +{tag_count}")

        # 4. 写入 discussion_comments
        comment_count = 0
        for topic_idx, comments in enumerate(COMMENTS):
            for j, (text, nick) in enumerate(comments):
                db.add(DiscussionComment(
                    topic_id=topic_id_map[topic_idx],
                    user_id=23 + j, nickname=nick, content=text,
                    created_at=now + timedelta(hours=topic_idx, minutes=j*5),
                ))
                comment_count += 1
        db.commit()
        print(f"[4] discussion_comments +{comment_count}")

        # 5. 写入 activities（10条）
        act_count = 0
        for i, act in enumerate(ACTIVITIES):
            start = now + timedelta(days=act["days_offset"], hours=act["hour"])
            end = start + timedelta(hours=act["duration_hours"])
            cover = make_cover(
                f"crs_demo_activity_{i+1}.svg",
                act["chapter"], act["title"], act["location"],
                "非遗体验活动", "线下参与",
            )
            db.add(Activity(
                title=act["title"], cover_url=cover, location=act["location"],
                organizer=act["organizer"], start_time=start, end_time=end,
                max_participants=act["max_participants"],
                description=act["description"], notes=act["notes"],
                is_featured=bool(act["is_featured"]), status=act["status"],
                created_at=now,
            ))
            act_count += 1
        db.commit()
        print(f"[5] activities +{act_count}")

        print()
        print("=" * 55)
        print("CRS演示数据注入完成！")
        print(f"  contents:               +{len(all_articles)} 条")
        print(f"  discussion_topics:       +{len(DISCUSSION_TOPICS)} 条")
        print(f"  discussion_topic_tags:   +{tag_count} 条")
        print(f"  discussion_comments:    +{comment_count} 条")
        print(f"  activities:              +{act_count} 条")
        print("=" * 55)
        print()
        print("演示结构设计说明：")
        print("  [A层 冷启动]  5条通用内容  → 零基础用户冷启动推荐")
        print("  [B层 兴趣方向] 6条分类内容  → mixed阶段兴趣分化")
        print("  [C层 精准兴趣] 4条垂直内容  → precision精准推荐")
        print("  [帖子 4类12条]  梯度热度+精选  → 社区推荐与行为闭环")
        print("  [活动 3类10条]  近期时间分布  → 线下体验推荐与活动卡片")

    finally:
        db.close()


if __name__ == "__main__":
    main()
