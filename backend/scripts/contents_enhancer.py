def make_cover_url(heritage_type: str, index: int) -> str:
    """生成SVG封面"""
    import random as _r
    PALETTES = [
        ("#261611", "#8D2A1D"),
        ("#1E2230", "#7D5A3D"),
        ("#2B1F30", "#A44E34"),
        ("#1D2A2F", "#7C633E"),
        ("#2E1B18", "#B26C42"),
    ]
    c1, c2 = _r.choice(PALETTES)
    svg = f"""<svg xmlns='http://www.w3.org/2000/svg' width='1200' height='720' viewBox='0 0 1200 720'>
  <defs><linearGradient id='bg' x1='0' y1='0' x2='1' y2='1'>
    <stop offset='0%' stop-color='{c1}'/><stop offset='100%' stop-color='{c2}'/>
  </linearGradient></defs>
  <rect width='1200' height='720' fill='url(#bg)' rx='34'/>
  <circle cx='920' cy='140' r='200' fill='rgba(255,255,255,0.08)'/>
  <text x='86' y='122' fill='#FFE4C0' font-size='30' font-family='Microsoft YaHei'>{heritage_type}</text>
  <text x='86' y='268' fill='#FFF8F0' font-size='64' font-weight='700' font-family='Microsoft YaHei'>{index:02d}</text>
</svg>"""
    cover_dir = BASE_DIR / "storage" / "covers"
    cover_dir.mkdir(parents=True, exist_ok=True)
    path = cover_dir / f"kb_cover_{index:02d}.svg"
    path.write_text(svg, encoding="utf-8")
    return f"/static/covers/kb_cover_{index:02d}.svg"


