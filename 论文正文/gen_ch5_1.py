def add_para(text):
    doc.add_paragraph(text)

def add_fig(caption):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(caption)
    run.font.size = Pt(10.5)

